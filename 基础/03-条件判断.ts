let a: number = 2;
if (a === 2) {
    console.log("2222222222222222222")
} else if (a === 1) {
    console.log("1111111111111111111111")
} else {
    console.log("33333333333333")
}

switch (a) {
    case 1: {
        console.log("1111111111111111");
        break;
    }
    case 2: {
        console.log("22222222222222");
        break;
    }
    case 3: {
        console.log("333333333333");
        break;
    }
    default : {
        console.log("66666666666666")
    }
}
